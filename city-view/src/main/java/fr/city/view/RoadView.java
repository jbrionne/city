package fr.city.view;

public class RoadView {
	
	private String command;
	private int xa;
	private int za;
	private int xb;
	private int zb;
	private String name;	
	
	public String getCommand() {
		return command;
	}
	public void setCommand(String command) {
		this.command = command;
	}
	
	public int getXa() {
		return xa;
	}
	public void setXa(int xa) {
		this.xa = xa;
	}
	public int getZa() {
		return za;
	}
	public void setZa(int za) {
		this.za = za;
	}
	public int getXb() {
		return xb;
	}
	public void setXb(int xb) {
		this.xb = xb;
	}
	public int getZb() {
		return zb;
	}
	public void setZb(int zb) {
		this.zb = zb;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	

}
