package fr.city.core;


public class Road {

	private int xa;
	private int za;
	private int xb;
	private int zb;	
	private String name;
	private int red;
	private int green;
	private int blue;
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getXa() {
		return xa;
	}
	public void setXa(int xa) {
		this.xa = xa;
	}
	public int getZa() {
		return za;
	}
	public void setZa(int za) {
		this.za = za;
	}
	public int getXb() {
		return xb;
	}
	public void setXb(int xb) {
		this.xb = xb;
	}
	public int getZb() {
		return zb;
	}
	public void setZb(int zb) {
		this.zb = zb;
	}
	public int getRed() {
		return red;
	}
	public void setRed(int red) {
		this.red = red;
	}
	public int getGreen() {
		return green;
	}
	public void setGreen(int green) {
		this.green = green;
	}
	public int getBlue() {
		return blue;
	}
	public void setBlue(int blue) {
		this.blue = blue;
	}	

}
